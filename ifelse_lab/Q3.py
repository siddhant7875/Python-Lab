# Program to calculate simple interest
# Given: Principal (P) = 200 Rs, Rate (R) = 5%, Time (T) = 5 years
principal_amount = 200  
rate_of_interest = 5    
time_period = 5        

# Step 2: Calculate simple interest using the formula: (P * R * T) / 100
simple_interest = (principal_amount * rate_of_interest * time_period) / 100

# Step 3: Displaying the result
print(f"The simple interest on Rs. {principal_amount} for {time_period} years at {rate_of_interest}% per annum is Rs. {simple_interest:.2f}.")
